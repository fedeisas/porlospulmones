		<h3>Centro de manzana:</h3> 
		<p>Espacio cuya proyección sobre el plano horizontal queda definida en forma y ubicación por las líneas de frente interno.</p> 
		<p>En este espacio la ocupación del suelo podrá ser como máximo la establecida por el FOS y las alturas de basamento correspondiente a cada distrito.</p> 

		<h3>El espacio centro de manzana:</h3> 
		<p>Es aquél cuya planta queda definida en forma y ubicación por las líneas de frente interno. Las áreas libres de edificación serán destinadas exclusivamente a espacio libre parquizado.</p> 
